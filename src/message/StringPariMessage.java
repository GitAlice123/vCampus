package view.message;

public class StringPariMessage {
    private String first;
    private String second;

    public StringPariMessage(String first, String second) {
        this.first = first;
        this.second = second;
    }

    public StringPariMessage() {
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
